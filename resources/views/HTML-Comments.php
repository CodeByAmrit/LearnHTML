<h2>Keyboard Shortcut for HTML Comments</h2>
<p>
  Most code editors (including Programiz's Online HTML Editor) have a keyboard
  shortcut for commenting code.
</p>
<p>
  In general, most code editors use Ctrl + / (on Windows or Linux) and Cmd + /
  (on Mac) for creating comments.
</p>
<p>
  We encourage you to remember these shortcuts as comments are used frequently
  to add extra information as well as to temporarily remove certain code.
</p>

