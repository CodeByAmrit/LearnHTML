<h1>HTML Comments</h1>
<p>HTML comments are used to insert notes to a web page. For example,</p>
<div class="showCode">
  <code> &lt;!-- heading 2 --&gt; &lt;h2&gt;Comments in HTML&lt;/h2&gt; </code>
</div>
<p>
  Here, &lt;!-- heading 2 --&gt; is a comment. In HTML, comments start with
  &lt;!-- and ends with --&gt;
</p>
<div class="showCode">
  <code>
    &lt;!-- an HTML div --&gt; &lt;div&gt; &lt;p&gt;HTML is fun to
    learn.&lt;/p&gt; &lt;/div&gt;
  </code>
</div>
<p>Browser Output</p>
<div class="showCode">
  <img src="images/html-comment.png" alt="comment" id="img" />
</div>
<p>Here,</p>
<div class="showCode">
  <code> &lt;!-- an HTML div --&gt; </code>
</div>
<p>is a comment. Hence, it is not displayed in the browser output.</p>
<hr />
<h2>Why use HTML Comments?</h2>
<p>
  Comments are mainly used to make our code more readable. They are completely
  hidden from the webpage and only show up in the code.
</p>
<p>
  Commenting on your code is a good practice as it helps us to express what the
  code is doing. It can act as an anchor for you if you want to change your code
  in the future.
</p>
<hr />
<h2>Single-Line and Multi-line Comments</h2>
<p>
  In HTML, we use the same syntax to create single-line and multi-line comments.
</p>
<p>Single Line Comment</p>
<div class="showCode">
  <code>
    &lt;!-- Write a heading --&gt; &lt;h1&gt;Important Heading&lt;/h1&gt;
  </code>
</div>
<p>Multi-line Comments</p>
<div class="showCode">
  <code>
    &lt;!-- Multiple Line comments can include line breaks and also extra spaces
    --&gt; &lt;p&gt;this will display in the webpage&lt;/p&gt;
  </code>
</div>
<hr />
<h2>HTML Tags Inside Comments</h2>
<p>
  If we put HTML elements inside comments, they will be ignored. For example,
</p>
<div class="showCode">
  <code>
    &lt;p&gt;this will be displayed&lt;/p&gt; &lt;!-- &lt;p&gt;this will not be
    displayed&lt;/p&gt; --&gt; &lt;p&gt;this will also be displayed&lt;/p&gt;
  </code>
</div>
