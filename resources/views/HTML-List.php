<h1>HTML Table</h1>
            <p>HTML lists are used to display related information in an easy-to-read and concise way as lists.</p>
            <div class="showCode">
                <img src="images/html-list-examples.png" alt="list" id="img">
            </div>
            <p>We can use three types of lists to represent different types of data in HTML:</p>
            <li>1. Unordered List &lt;ul&gt;</li>
            <li>2. Ordered List &lt;ol&gt;>&lt;/ol></li>
            <li>3. Description List &lt;dl&gt;</li>
            <hr>
            <h2>Unordered List</h2>
            <p>The unordered list is used to represent data in a list for which the order of items does not matter.</p>
            <p>In HTML, we use the &lt;ul&gt; tag to create unordered lists. Each item of the list must be a &lt;li&gt; tag which represents list items. For example,</p>
            <div class="showCode">
<code>
&lt;ul&gt;
  &lt;li&gt;Apple&lt;/li&gt;
  &lt;li&gt;Orange&lt;/li&gt;
  &lt;li&gt;Mango&lt;/li&gt;
&lt;/ul&gt;

</code>
            </div>
            <p>Browser Output</p>
            <div class="showCode">
                <img src="images/html-lists-unordered.png" alt="unordered list" id="img">
            </div>
            <p>Here, &lt;li&gt;Apple&lt;/li&gt;, &lt;li&gt;Orange&lt;/li&gt;, and &lt;li&gt;Mango&lt;/li&gt; are the list items.</p>
            <hr>

            <h2>Ordered List</h2>
            <p>The ordered list is used to represent data in a list for which the order of items has significance.</p>
            <p>The &lt;ol&gt; tag is used to create ordered lists. Similar to unordered lists, each item in the ordered list must be a &lt;li&gt; tag. For example,</p>
            <div class="showCode">
<code>
&lt;ol&gt;
    &lt;li&gt;Ready&lt;/li&gt;
    &lt;li&gt;Set&lt;/li&gt;
    &lt;li&gt;Go&lt;/li&gt;
&lt;/ol&gt;
</code>
            </div>
            <p>Browser Output</p>
            <div class="showCode">
                <img src="images/html-list-ordered.png" alt="ordered" id="img">
            </div>
            <p>Here, you can see list items are represented with numbers to represent a certain order.</p>
            <hr>
            <h2>Description List</h2>
            <p>The HTML description list is used to represent data in the name-value form. We use the &lt;dl&gt; tag to create a definition list and each item of the description list has two elements:</p>
            <li>term/title - represented by the &lt;dt&gt; tag</li>
            <li>description of the term - represented by the &lt;dd&gt; tag</li>
            <p>Let's see an example,</p>
            <div class="showCode">
<code>
&lt;dl&gt;
  &lt;dt&gt;HTML&lt;/dt&gt;
  &lt;dd&gt;Hyper-Text Markup Language&lt;/dd&gt;
  &lt;dt&gt;CSS&lt;/dt&gt;
  &lt;dd&gt;Cascading StyleSheets&lt;/dd&gt;
  &lt;dt&gt;JS&lt;/dt&gt;
  &lt;dd&gt;Javascript&lt;/dd&gt;
&lt;/dl&gt;

</code>
            </div>
            <p>Browser Output</p>
            <div class="showCode">
                <img src="images/html-list-description.png" alt="discription list" id="img">
            </div>
            <p>Since the description list includes the definition of a term, it is also known as the definition</p>
