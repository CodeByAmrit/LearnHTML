<h1>HTML Headings</h1>
<p>
  The HTML heading tags (&lt;h1&gt; to &lt;h6&gt;) are used to add headings to a
  webpage. For example,
</p>
<div class="showCode">
  <code>
    &lt;h1&gt;This is heading 1.&lt;/h1&gt; &lt;h2&gt;This is heading
    2.&lt;/h2&gt;>&lt;/h2> &lt;h3&gt;This is heading 3.&lt;/h3&gt;
    &lt;h4&gt;This is heading 4.&lt;/h4&gt; &lt;h5&gt;This is heading
    5.&lt;/h5&gt; &lt;h6&gt;This is heading 6.&lt;/h6&gt;
  </code>
</div>
<p>Browser Output</p>
<div class="showCode">
  <img src="images/headings-in-html.png" alt="heading" id="img" />
</div>
<p>
  In the example, we have used tags &lt;h1&gt; to &lt;h6&gt; to create headings
  of varying sizes and importance.
</p>
<p>
  The &lt;h1&gt; tag denotes the most important heading on a webpage. Similarly,
  &lt;h6&gt; denotes the least important heading.
</p>
<p>
  The difference in sizes of heading tags comes from the browser's default
  styling. And, you can always change the styling of heading tags, including
  font size, using CSS.
</p>
<hr />

<h2>h1 Tag is Important</h2>
<p>The HTML &lt;h1&gt; tag defines the most important heading in a webpage.</p>
<p>
  Although it's possible to include multiple &lt;h1&gt; tags in a webpage, the
  general practice is to use a single &lt;h1&gt; tag (usually at the beginning
  of the page).
</p>
<p>
  The &lt;h1&gt; tag is also important for SEO. Search engines (such as Google)
  treat content inside &lt;h1&gt; with greater importance compared to other
  tags.
</p>
<hr />
<h2>Headings are block-level elements</h2>
<p>
  The Headings &lt;h1&gt; to &lt;h6&gt; tags are block-level elements. They
  start on a new line and take up the full width of their parent element. For
  example,
</p>
<div class="showCode">
  <code>
    &lt;h1&gt;Headings&lt;/h1&gt; &lt;h2&gt;are&lt;/h2&gt;
    &lt;h3&gt;fun!&lt;/h3&gt;
  </code>
</div>
